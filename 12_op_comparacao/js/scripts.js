
var idade = 16;
var idadeMinima = 18;

// dica: no js o 1 é identificado como true, e o 0 é false
// var variavel = 1; true
// var variavel = 0; false

// Operador IF >=
if(idade >= idadeMinima) {
    console.log("Apto para fazer a CNH!");
} else {
    console.log("Não é apto para fazer a 1ª CNH!");
}

// Operador IF <=
if(idade <= idadeMinima) {
    console.log("Não é apto para fazer a 1ª CNH!");
} else {
    console.log("Apto para fazer a CNH!");
}

// Operador IF ==
var nome = "Adriano";

if (nome == "Adriano") {
    console.log("Seu nome é Adriano");
}

// Operador IF !=
if (nome != "Adriano") {
    console.log("Seu nome é Adriano");
}

// Operador IF >
