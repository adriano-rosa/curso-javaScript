// condição if else
var nome = "Adriano";

if(nome == "Pedro") {
    console.log("Seu nome é Pedro!");
} else {
    console.log("Seu nome não é Pedro!");
}

// condição if elseIF
if(nome == "Pedro") {
    console.log("Seu nome é Pedro!");
} else if (nome == "Adriano") {
    console.log("Correto! Seu nome é Adriano!");
} else {
    console.log("Seu nome não é Pedro!");
}