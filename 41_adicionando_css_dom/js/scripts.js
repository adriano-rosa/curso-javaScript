// como adicionar css ao título principal

// selecionar o elemento - #title
var title = document.querySelector("#title");

// adicionar o css para uma propriedade desse objeto (adiciona o estilo)
title.style.color = "red";

// background-color
title.style.backgroundColor = "yellow";


// selecionar o elemento
var subtitle = document.querySelector(".subtitle");

// adicionar vários estilos
subtitle.style.cssText = "color: blue; background-color: pink; font-size: 50px";



